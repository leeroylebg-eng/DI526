# 1. What is a module?
# A module is a Python file that contains code (functions, variables, classes)
# that can be reused in other Python programs.

# 2. List three ways to import a module in Python:
# import module_name
# import module_name as alias
# from module_name import specific_function

# 3. What is the purpose of importing?
# Importing allows us to use code (functions, classes, variables)
# that are defined in another module.

# 4. List three examples when you would use the random module:
# - Generating a random number (e.g., random.randint)
# - Shuffling a list (e.g., random.shuffle)
# - Selecting a random item from a list (e.g., random.choice)

# 5. What is an ImportError?
# An ImportError happens when Python cannot find or load a module
# that you are trying to import.

# 6. When would using an OrderedDict be useful?
# OrderedDict is useful when you need to maintain the order
# in which items were added to a dictionary.

# 7. When would using a defaultdict be useful?
# defaultdict is useful when you want a dictionary that automatically
# provides a default value for a missing key.

